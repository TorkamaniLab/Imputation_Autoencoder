def convert_gt_to_int(gt, alt_signal_only=False, one_hot=False, dosage=False):
    if one_hot is True:
        return genotype_to_one_hot[gt[0:3]]
    if dosage is True:
        return genotype_to_dosage[gt[0:3]]
    if alt_signal_only is True:
        return genotype_to_int_alt_signal_only[gt[0:3]]
    return genotype_to_dip[gt[0:3]]
